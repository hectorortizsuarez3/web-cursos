<!-- Pregunta 1 -->
    <button class="faq-question">¿Cómo puedo matricularme en uno de los cursos? </button>
    <div class="faq-answer" hidden>En este momento, las matrículas se gestionan de forma manual. Para inscribirte en un curso, solo tienes que contactar con nosotros por teléfono o correo electrónico (ver sección de Contacto). Te enviaremos un formulario de inscripción y las instrucciones de pago.</div>

<!-- Pregunta 2 -->
<button class="faq-question">¿Qué incluyen exactamente los precios indicados en cada curso?</button>
<div class="faq-answer" hidden>Cada precio incluye acceso completo a los contenidos del curso, actividades prácticas, seguimiento por parte del equipo docente, tutorías semanales, soporte por correo y certificado final. No existen costes ocultos ni pagos adicionales.</div>

<!-- Pregunta 3 -->
<button class="faq-question">¿Qué métodos de pago aceptáis y hay opciones de financiación?</button>
<div class="faq-answer" hidden>Aceptamos tarjetas Visa, MasterCard, pagos por PayPal y transferencia bancaria. Además ofrecemos pago fraccionado en 3 plazos sin intereses para importes superiores a 200 €: 1/3 al matricularte, 1/3 a mitad de curso y el último 1/3 antes de la fecha de fin.</div>

<!-- Pregunta 4 -->
<button class="faq-question">¿Los precios incluyen IVA?</button>
<div class="faq-answer" hidden>Sí. Todos los precios que ves en las tarjetas de los cursos incluyen el IVA legal vigente. No se aplicarán recargos adicionales.</div>

<!-- Pregunta 5 -->
<button class="faq-question">¿Cómo y cuándo recibo mi certificado y los ECTS?</button>
<div class="faq-answer" hidden>Una vez completes al menos el 80 % de los contenidos y apruebes el test final, generamos automáticamente un certificado digital firmado. El de 90 h (HTML & CSS) convalida 3 ECTS; los de 250 h (Bases de Datos y JavaScript) convalidan 10 ECTS cada uno.</div>

<!-- Pregunta 6 -->
<button class="faq-question">¿Cuál es el plazo de matrícula y qué ocurre si me matriculo tarde?</button>
<div class="faq-answer" hidden>Cada curso tiene su fecha límite de matrícula (por ejemplo, HTML & CSS hasta el 16/09/2025). Si te matriculas después de esa fecha, pasarás a lista de espera y solo podrás acceder si queda alguna plaza libre. En ese caso, podría aplicarse un recargo del 10 %.</div>

<!-- Pregunta 7 -->
<button class="faq-question">¿Cómo están estructurados los contenidos y puedo acceder 24/7?</button>
<div class="faq-answer" hidden>Sí, el acceso es completamente online y disponible 24/7. Cada curso está organizado en lecciones teóricas, ejercicios prácticos, pruebas de autoevaluación y un proyecto final. Puedes avanzar a tu ritmo dentro del periodo de duración del curso.</div>

<!-- Pregunta 8 -->
<button class="faq-question">¿Qué requisitos técnicos necesito?</button>
<div class="faq-answer" hidden>Solo necesitas un navegador actualizado (como Chrome, Firefox o Edge) y una conexión a Internet estable. No es necesario instalar programas específicos: puedes utilizar cualquier editor de código que prefieras, como Visual Studio Code o Sublime Text.</div>

<!-- Pregunta 9 -->
<button class="faq-question">¿Ofrecéis soporte y tutorías personalizadas?</button>
<div class="faq-answer" hidden>Sí. Dispones de soporte por correo electrónico con respuesta en menos de 24 h, acceso a un foro privado de alumnos, y tutorías en directo todos los viernes a las 18:00 vía Zoom. Las plazas para las tutorías se reservan semanalmente desde tu área de curso.</div>

<!-- Pregunta 10 -->
<button class="faq-question">¿Hay descuentos para grupos o convenios con empresas?</button>
<div class="faq-answer" hidden>Sí. Ofrecemos descuentos del 5 % al 15 % para empresas o grupos de más de 5 personas. Si deseas recibir un presupuesto adaptado, puedes escribirnos a empresas@tudominio.com o usar los datos de contacto proporcionados en esta web.</div>



